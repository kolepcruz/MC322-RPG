public class Mage {
    private int mana=10;
    private int contMana=10;

    public Mage() {
        this.contMana = 10;
    }

    public int getContMana() {
        return contMana;
    }

    public void setContMana(int contMana) {
        this.contMana = contMana;
    }

    public int getMana() {
        return mana;
    }

    public void setMana(int mana) {
        this.mana = mana;
    }
}
