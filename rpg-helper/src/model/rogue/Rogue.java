public class Rogue {
    private int vigor=15;
    private int contVigor;

    public Rogue() {
        this.contVigor = 15;
    }

    public int getVigor() {
        return vigor;
    }

    public void setVigor(int vigor) {
        this.vigor = vigor;
    }

    public int getContVigor() {
        return contVigor;
    }

    public void setContVigor(int contVigor) {
        this.contVigor = contVigor;
    }
}
