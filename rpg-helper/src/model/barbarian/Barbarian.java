public class Barbarian {
    private int furia=20;
    private int contFuria;

    public Barbarian() {
        this.contFuria = 20;
    }

    public int getFuria() {
        return furia;
    }

    public void setFuria(int furia) {
        this.furia = furia;
    }

    public int getContFuria() {
        return contFuria;
    }

    public void setContFuria(int contFuria) {
        this.contFuria = contFuria;
    }
}
